package xyz.flaflo.ytp.video.thumbnail;

/**
 * Represents the YouTube thumbnail format
 * @author Flaflo
 */
public enum YouTubeThumbnailFormat {
    
    /**
     * The default resolution
     */
    DEFAULT,
    
    /**
     * The medium resolution
     */
    MEDIUM,
    
    /**
     * The high resolution
     */
    HIGH,
    
    /**
     * The standard resolution
     */
    STANDARD,
    
    /**
     * The max resolution
     */
    MAXRES
}
